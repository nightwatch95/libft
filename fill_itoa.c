#include "libft.h"

char	*fill_itoa(char *res, int n)
{
	if (n == -2147483648)
		return (ft_strcpy(res, "-2147483648"));
	if (n < 0)
	{
		n *= -1;
		*res++ = '-';
	}
	if (n > 9)
	{
		res = fill_itoa(res, n / 10);
		*res = n % 10 + '0';
		return (res + 1);
	}
	else
	{
		*res = n + '0';
		return (res + 1);
	}
	return (res);
}
