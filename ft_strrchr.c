#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*t;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			t = (char *)(s + i);
		i++;
	}
	if (*t == (char)c)
		return (t);
	return (NULL);
}
